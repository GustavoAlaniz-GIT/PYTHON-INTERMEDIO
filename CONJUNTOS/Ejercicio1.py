# Dados dos conjuntos A y B, escribe un programa en Python que imprima los elementos que se encuentran
# en A o en B, o en ambos.

# Definimos los conjuntos A y B
A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}

# Calculamos la unión de A y B
union = A | B

# Imprimimos los elementos que están en A o en B, o en ambos
print("Elementos que están en A o en B, o en ambos:", union)